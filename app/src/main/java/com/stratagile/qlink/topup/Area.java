package com.stratagile.qlink.topup;

public class Area {

    /**
     * en : Afghanistan
     * cn : 阿富汗
     * code : +93
     */

    private String en;
    private String cn;
    private String code;

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getCn() {
        return cn;
    }

    public void setCn(String cn) {
        this.cn = cn;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
